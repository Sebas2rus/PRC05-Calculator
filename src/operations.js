export function addition(num1, num2) {
    num1 = Number(num1)
    num2 = Number(num2)
    return (num1 + num2)
}

export function substraction(num1, num2) {
    num1 = Number(num1)
    num2 = Number(num2)
    return (num1 - num2)
}

export function multiplication(num1, num2) {
    num1 = Number(num1)
    num2 = Number(num2)
    return (num1 * num2)
}

export function division(num1, num2) {
    num1 = Number(num1)
    num2 = Number(num2)
    return (num1 / num2)
}